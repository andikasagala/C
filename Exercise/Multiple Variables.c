#include <stdio.h>

// Multiple Variables

int main () {

	int Var1; // declare the variable 1 by using INT
	Var1 = 1; // declare the variable value
	int Var2; // declare the variable 2 by using INT
	Var2 = 2; // declare the variable value
	int Var3; // declare the variable 3 by using INT
	Var3 = 3; // declare the variable value

	printf("Var1 value is %d \n", Var1); // print out the Var1 value
	printf("Var2 value is %d \n", Var2); // print out the Var2 value
	printf("Var3 value is %d \n", Var3); // print out the Var3 value

	// or
	printf("\n \n"); // giving newline by use "\n"
	printf("Var1 value is %d \n Var2 value is %d \n Var3 value is %d \n", Var1, Var2, Var3); // print out the Var1, Var2, Var3 values
	
	return 0;
}